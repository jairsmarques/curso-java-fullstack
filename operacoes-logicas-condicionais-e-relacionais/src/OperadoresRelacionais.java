
public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int nota1 = 90;
		int nota2 = 90; 
		
		// == -> igual
		// != -> diferente
		// <  -> menor
		// >  -> maior
		// <= -> menor ou igual
		// >= -> maior ou igual
		
		if (nota1 == nota2) {
			System.out.println("As notas são iguais");
		} else {
			System.out.println("As notas são diferentes");
		}
	}

}
